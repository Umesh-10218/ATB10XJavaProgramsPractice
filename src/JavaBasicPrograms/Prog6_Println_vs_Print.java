package JavaBasicPrograms;

public class Prog6_Println_vs_Print {
    public static void main(String[] args) {
        //Println--> Always adds new line in console after every execution
        System.out.println("Krishna");
        System.out.println("Rukmini");
        //Print--> No new line in console after every execution
        System.out.print("Krishna");
        System.out.print("Rukmini");
    }
}
